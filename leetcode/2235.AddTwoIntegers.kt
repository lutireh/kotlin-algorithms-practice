/* 
 * Problem:
 * Given two integers num1 and num2, return the sum of the two integers.
 *
 * Approach:
 * Return the sum of the two given integers.
 * 
 * Time Complexity: O(1)
 * Space Complexity: O(1)
 */

 class Solution {
    fun sum(num1: Int, num2: Int): Int {
        return num1 + num2
    }
}